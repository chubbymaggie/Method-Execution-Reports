package de.uni_stuttgart.cer.generator;

// Method caller information
public class MethodCaller
{
	// Variables
	public int id;
	public MethodName methodName;
	
	// Create method caller
	MethodCaller(int id, MethodName methodName)
	{
		this.id = id;
		this.methodName = methodName;
	}
}